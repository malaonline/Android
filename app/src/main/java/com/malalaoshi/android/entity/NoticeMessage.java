public class NoticeMessage {
    private Long unpaid_num;
    private Long tocomment_num;

    public Long getUnpaid_num() {
        return unpaid_num;
    }

    public void setUnpaid_num(Long unpaid_num) {
        this.unpaid_num = unpaid_num;
    }

    public Long getTocomment_num() {
        return tocomment_num;
    }

    public void setTocomment_num(Long tocomment_num) {
        this.tocomment_num = tocomment_num;
    }

    @Override
    public String toString() {
        return "NoticeMessage{" +
                "unpaid_num=" + unpaid_num +
                ", tocomment_num=" + tocomment_num +
                '}';
    }
}
